package forest;

public class Node {
    private int no;
    private String name;

    public Node(int no, String name) {
        this.no = no;
        this.name = name;
    }

    // ゲッター ------------------
    public int getNo() {
        return no;
    }

    public String getName() {
        return name;
    }
    //----------------------------
}
